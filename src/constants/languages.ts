export function getTranslation(language: AppLanguage, key: TranslationKey, params?: Params): string {
  const template = translations[language][key as keyof (typeof translations)[typeof language]] ?? translations.en[key as keyof typeof translations.en] ?? key;
  if (!params) return template as string;
  return Object.entries(params).reduce((value, [paramKey, paramValue]) => value.replace(new RegExp(`\\{${paramKey}\\}`, 'g'), String(paramValue)), String(template));
}


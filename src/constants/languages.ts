export function formatDemoScenarioTitle(language: AppLanguage, scenario: 'smooth' | 'missed' | 'no-response' | 'escalated'): string { const key = scenario === 'no-response' ? 'noResponse' : scenario; return getTranslation(language, `demo.${key}.title`); }
export function formatDemoScenarioDetail(language: AppLanguage, scenario: 'smooth' | 'missed' | 'no-response' | 'escalated'): string { const key = scenario === 'no-response' ? 'noResponse' : scenario; return getTranslation(language, `demo.${key}.detail`); }
export function formatDemoScenarioSummary(language: AppLanguage, scenario: 'smooth' | 'missed' | 'no-response' | 'escalated'): string { const key = scenario === 'no-response' ? 'noResponse' : scenario; return getTranslation(language, `demo.${key}.summary`); }
export function formatLanguageName(language: AppLanguage, code: AppLanguage): string { return getTranslation(language, code === 'te' ? 'common.languageTelugu' : 'common.languageEnglish'); }
export function localizeKnownText(language: AppLanguage, text: string): string {
  if (!text || language === 'en') return text;
  const exactMap: Record<string, string> = {
    'Using local demo data. Check the MedBridge backend connection.': getTranslation(language, 'messages.usingLocalDemo'),
    'Backend upload is unavailable. Using local sample document.': getTranslation(language, 'messages.backendUploadUnavailable'),
    'Backend schedule sync failed. Using local demo schedule.': getTranslation(language, 'messages.backendScheduleUnavailable'),
    'Dose update could not reach the backend. Showing local progress only.': getTranslation(language, 'messages.doseUpdateOffline'),
    'Demo schedule ready': getTranslation(language, 'messages.demoScheduleReadyTitle'),
    'The sample document created a medicine plan for today.': getTranslation(language, 'messages.demoScheduleReadyDetail'),
    'Two or more doses were marked missed today. Caregiver follow-up is recommended.': getTranslation(language, 'messages.caregiverAlertReason'),
    'Multiple doses are still unconfirmed. A caregiver check-in may be helpful.': getTranslation(language, 'messages.caregiverWatchReason'),
    'No caregiver escalation is active right now.': getTranslation(language, 'messages.caregiverOkayReason'),
    'Caregiver alert triggered': getTranslation(language, 'messages.caregiverAlertTriggered'),
    'Caregiver watch triggered': getTranslation(language, 'messages.caregiverWatchTriggered'),
    'Schedule generated': getTranslation(language, 'messages.scheduleGeneratedTitle'),
    'Medicines were converted into a morning, afternoon, and night plan.': getTranslation(language, 'messages.scheduleGeneratedDetail'),
    'One line may need manual review': getTranslation(language, 'messages.oneLineNeedsReview'),
    'Most doses were marked taken and no caregiver follow-up is needed.': getTranslation(language, 'messages.smoothHistoryDetail'),
    'Caregiver attention may be needed because multiple doses were missed.': getTranslation(language, 'messages.missedHistoryDetail'),
    'No response recorded': getTranslation(language, 'messages.noResponseHistoryTitle'),
    'Reminder was not answered and doses were marked unconfirmed.': getTranslation(language, 'messages.noResponseHistoryDetail'),
    'Caregiver escalation is active': getTranslation(language, 'messages.escalatedHistoryTitle'),
    'Multiple missed and unconfirmed doses are visible across the day.': getTranslation(language, 'messages.escalatedHistoryDetail'),
    'Uploaded now': getTranslation(language, 'common.uploadedNow'),
    'Issued today': getTranslation(language, 'common.issuedToday'),
    'Discharge Summary Sample': getTranslation(language, 'upload.documentTitle'),
    'Printed discharge instructions with 4 medicine lines and one unclear entry.': getTranslation(language, 'upload.documentSummary'),
    'All medicines handled.': getTranslation(language, 'common.allHandled'),
    'All doses on track.': getTranslation(language, 'caregiver.onTrackDetail'),
    'No pending items.': getTranslation(language, 'common.noPendingItems'),
    'No activity yet.': getTranslation(language, 'common.noActivity'),
    'No pending doses.': getTranslation(language, 'common.noPendingDoses'),
    'No active reminders.': getTranslation(language, 'common.noActiveReminders'),
    'Dose marked taken.': getTranslation(language, 'reminder.takenFeedback'),
    'Dose marked missed.': getTranslation(language, 'reminder.missedFeedback'),
    'Dose marked unconfirmed.': getTranslation(language, 'reminder.unconfirmedFeedback'),
    'Reminder will repeat.': getTranslation(language, 'reminder.repeatFeedback'),
    'Voice replayed.': getTranslation(language, 'reminder.voiceReplayed'),
    'Playing voice reminder': getTranslation(language, 'reminder.voiceTitle'),
    'Clear pages work best': getTranslation(language, 'upload.helper'),
  };
  if (exactMap[text]) return exactMap[text];
  const detected = text.match(/^(\d+) detected lines ready for review\.$/i); if (detected) return getTranslation(language, 'messages.detectedLinesSummary', { count: detected[1] });
  const reminder = text.match(/^Reminder opened for (.+) at (.+)\.$/); if (reminder) return getTranslation(language, 'messages.reminderOpenedDetail', { name: reminder[1], time: reminder[2] });
  const taken = text.match(/^(.+) was marked taken for (.+)\.$/); if (taken) return getTranslation(language, 'messages.doseTakenDetail', { name: taken[1], time: taken[2] });
  const missed = text.match(/^(.+) was marked missed for (.+)\.$/); if (missed) return getTranslation(language, 'messages.doseMissedDetail', { name: missed[1], time: missed[2] });
  const pending = text.match(/^(.+) is back in the pending list for review\.$/); if (pending) return getTranslation(language, 'messages.dosePendingDetail', { name: pending[1] });
  const replay = text.match(/^(.+) voice reminder was replayed for the patient\.$/); if (replay) return getTranslation(language, 'messages.voiceReminderReplayedDetail', { name: replay[1] });
  const delay = text.match(/^(.+) reminder will appear again soon\.$/); if (delay) return getTranslation(language, 'messages.reminderDelayedDetail', { name: delay[1] });
  return formatInstructionLabel(language, text);
}
